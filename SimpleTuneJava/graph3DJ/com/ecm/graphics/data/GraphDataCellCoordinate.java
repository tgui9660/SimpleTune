package com.ecm.graphics.data;

public class GraphDataCellCoordinate {
	private int x;
	private int z;
	
	public int getX() {
		return x;
	}

	public int getZ() {
		return z;
	}

	public GraphDataCellCoordinate(int x, int z){
		this.x = x;
		this.z = z;
	}
	
	public boolean coordEquals(GraphDataCellCoordinate gcc){
		if((this.x == gcc.getX()) && (this.z == gcc.getZ())){
			return true;
		}
		
		return false;
	}
	
}
